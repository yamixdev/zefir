def docs_hash() -> str:
    h = sha256()
    h.update(TOS_VERSION.encode())
    h.update(b"\n")
    h.update(TOS_TEXT.encode())
    h.update(b"\n")
    h.update(PRIVACY_TEXT.encode())
    return h.hexdigest()[:16]


